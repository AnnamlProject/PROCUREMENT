import * as React from "react"

import { cn } from "@/lib/utils"

interface TabsProps extends React.HTMLAttributes<HTMLDivElement> {
    value: string;
    onValueChange: (value: string) => void;
}

const TabsContext = React.createContext<{ value: string; onValueChange: (value: string) => void; } | null>(null);

const useTabs = () => {
    const context = React.useContext(TabsContext);
    if (!context) throw new Error("Tabs components must be used within a Tabs provider");
    return context;
}

// FIX: Replaced the component with a `React.forwardRef` implementation to correctly handle the `ref` prop and fix related type errors.
const Tabs = React.forwardRef<HTMLDivElement, TabsProps>(({ className, value, onValueChange, ...props }, ref) => (
    <TabsContext.Provider value={{ value, onValueChange }}>
        <div
            ref={ref}
            className={cn("w-full", className)}
            {...props}
        />
    </TabsContext.Provider>
));
Tabs.displayName = "Tabs";

const TabsList = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    role="tablist"
    className={cn(
      "inline-flex h-10 items-center justify-center rounded-md bg-muted p-1 text-muted-foreground",
      className
    )}
    {...props}
  />
))
TabsList.displayName = "TabsList"

const TabsTrigger = React.forwardRef<
  HTMLButtonElement,
  React.ButtonHTMLAttributes<HTMLButtonElement>
>(({ className, value, ...props }, ref) => {
    const { value: activeValue, onValueChange } = useTabs();
    const isActive = activeValue === value;
    return (
        <button
            ref={ref}
            type="button"
            role="tab"
            aria-selected={isActive}
            onClick={() => onValueChange(value!)}
            className={cn(
            "inline-flex items-center justify-center whitespace-nowrap rounded-sm px-3 py-1.5 text-sm font-medium ring-offset-background transition-all focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
            isActive ? "bg-background text-foreground shadow-sm" : "hover:bg-background/50",
            className
            )}
            {...props}
        />
    )
})
TabsTrigger.displayName = "TabsTrigger"

const TabsContent = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, value, ...props }, ref) => {
    const { value: activeValue } = useTabs();
    if (activeValue !== value) return null;
    return (
        <div
            ref={ref}
            role="tabpanel"
            className={cn(
            "mt-2 ring-offset-background focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2",
            className
            )}
            {...props}
        />
    )
})
TabsContent.displayName = "TabsContent"


export { Tabs, TabsList, TabsTrigger, TabsContent }